PUBLIC STRING
EXTERN OUTPUT: FAR

STACKSEG SEGMENT PARA STACK 'STACK'
	DB 256 DUP(0)
STACKSEG ENDS

BUFSEG SEGMENT PARA PUBLIC 'DATA'
	MAXLEN DB 255
	CURLEN DB 0
	STRING DB 256 DUP('$')
BUFSEG ENDS

CODESEG SEGMENT PARA PUBLIC 'CODE'
	ASSUME CS:CODESEG, DS:BUFSEG, SS:STACKSEG
MAIN:
	MOV AX, BUFSEG
	MOV DS, AX
	MOV DX, 0

	MOV AH, 0Ah
	INT 21h

	CALL OUTPUT

	MOV AH, 4Ch
	INT 21h
CODESEG ENDS
END MAIN