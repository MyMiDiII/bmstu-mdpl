EXTERN STRING:BYTE
PUBLIC OUTPUT

OUTCODESEG SEGMENT PARA PUBLIC	'CODE'
    ASSUME CS:OUTCODESEG
OUTPUT PROC FAR
    MOV AH, 2
    MOV DL, 13
    INT 21h
    
    MOV DL, 10
    INT 21h
    
    MOV SI, 0
    MOV CX, 7
SEVEN:
    MOV DL, STRING[SI]
    INT 21h
    
    INC SI
    LOOP SEVEN
    
    RET
OUTPUT ENDP
OUTCODESEG ENDS
END
