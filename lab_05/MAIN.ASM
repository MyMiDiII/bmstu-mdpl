EXTERN READ_ROWS:FAR
EXTERN READ_COLS:FAR
EXTERN READ_MATRIX:FAR
EXTERN TRANSFORM_MATRIX:FAR
EXTERN WRITE_MATRIX:FAR 

STACKSEG SEGMENT PARA STACK 'STACK'
    DB 256 DUP(0)
STACKSEG ENDS

CODESEG SEGMENT	PARA PUBLIC 'CODE'
    ASSUME	CS:CODESEG, SS:STACKSEG
MAIN:
    CALL READ_ROWS
    CALL READ_COLS
    CALL READ_MATRIX
    CALL TRANSFORM_MATRIX
    CALL WRITE_MATRIX

    MOV AH, 4Ch
    INT 21h
CODESEG ENDS
END MAIN
